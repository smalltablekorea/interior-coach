#!/usr/bin/env node
// ko.json 의 키 구조를 기반으로 TS 타입을 생성.
//   - src/i18n/messages.generated.ts 에 IntlMessages 인터페이스 생성
//   - next-intl 의 augmentation 으로 t("...") 호출에 자동완성/타입체크 적용
//
// prebuild 단계에서 실행 — ko.json 변경 시 타입도 같이 갱신됨.

import fs from "node:fs";
import path from "node:path";

const SRC = path.resolve(process.cwd(), "messages", "ko.json");
const DEST = path.resolve(process.cwd(), "src", "i18n", "messages.generated.d.ts");

if (!fs.existsSync(SRC)) {
  console.error(`[i18n:types] messages/ko.json 이 없습니다`);
  process.exit(1);
}

const ko = JSON.parse(fs.readFileSync(SRC, "utf8"));

function buildType(obj, indent = "  ") {
  const lines = [];
  for (const [k, v] of Object.entries(obj)) {
    const safeKey = /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(k) ? k : JSON.stringify(k);
    if (v !== null && typeof v === "object" && !Array.isArray(v)) {
      lines.push(`${indent}${safeKey}: {`);
      lines.push(buildType(v, indent + "  "));
      lines.push(`${indent}};`);
    } else {
      lines.push(`${indent}${safeKey}: string;`);
    }
  }
  return lines.join("\n");
}

const banner = `// AUTO-GENERATED by scripts/i18n-gen-types.mjs — DO NOT EDIT BY HAND
// 출처: messages/ko.json (ko 가 단일 출처. en 은 i18n:check 로 동기화 강제)
// 갱신: npm run i18n:types (또는 prebuild 시 자동 실행)

`;

const content = `${banner}interface IntlMessages {
${buildType(ko)}
}

declare module "next-intl" {
  // next-intl 의 AppConfig 에 Messages 타입 주입 — t("...") 호출 시 키 자동완성/타입체크.
  interface AppConfig {
    Messages: IntlMessages;
  }
}
`;

fs.mkdirSync(path.dirname(DEST), { recursive: true });
fs.writeFileSync(DEST, content, "utf8");
console.log(`[i18n:types] wrote ${path.relative(process.cwd(), DEST)}`);
