/**
 * Get random hooks from a specific category
 */
export function getRandomHooks(category: string, count: number): string[] {
  const categoryTemplate = HOOK_TEMPLATES.find(
    (t) => t.category === category
  );
  if (!categoryTemplate) return [];

  const hooks = [...categoryTemplate.hooks];
  const result: string[] = [];
  const maxCount = Math.min(count, hooks.length);

  for (let i = 0; i < maxCount; i++) {
    const randomIndex = Math.floor(Math.random() * hooks.length);
    result.push(hooks[randomIndex]);
    hooks.splice(randomIndex, 1);
  }

  return result;
}

/**
 * Get all hooks for a given category
 */
export function getHooksByCategory(category: string): string[] {
  const categoryTemplate = HOOK_TEMPLATES.find(
    (t) => t.category === category
  );
  return categoryTemplate ? categoryTemplate.hooks : [];
}

/**
 * Get all available hook categories
 */
export function getAllCategories(): string[] {
  return HOOK_TEMPLATES.map((t) => t.category);
}

/**
 * Get a post template by its ID
 */
export function getPostTemplate(id: string): PostTemplate | undefined {
  return POST_TEMPLATES.find((t) => t.id === id);
}

/**
 * Fill a post template with provided variable values
 */
export function fillTemplate(
  template: PostTemplate,
  values: Record<string, string>
): string {
  let result = template.structure;

  // Replace all variables in the template
  for (const [key, value] of Object.entries(values)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, "g"), value);
  }

  // Replace hashtag placeholder with formatted hashtags
  const hashtags = template.hashtags.map((h) => `#${h}`).join(" ");
  result = result.replace("{해시태그}", hashtags);

  return result;
}

/**
 * Get all tip templates for a given category
 */
export function getTipsByCategory(category: string): TipTemplate[] {
  return TIP_TEMPLATES.filter((t) => t.category === category);
}

/**
 * Get all available tip categories
 */
export function getAllTipCategories(): string[] {
  return [...new Set(TIP_TEMPLATES.map((t) => t.category))];
}

/**
 * Get a random tip from a specific category
 */
export function getRandomTip(category: string): TipTemplate | undefined {
  const tips = getTipsByCategory(category);
  if (tips.length === 0) return undefined;
  return tips[Math.floor(Math.random() * tips.length)];
}

/**
 * Format a tip template into a ready-to-post string
 */
export function formatTipAsPost(tip: TipTemplate): string {
  const hashtags = tip.hashtags.map((h) => `#${h}`).join(" ");
  return `${tip.title}\n\n${tip.content}\n\n${hashtags}`;
}

/**
 * Get total hook count across all categories
 */
export function getTotalHookCount(): number {
  return HOOK_TEMPLATES.reduce((sum, t) => sum + t.hooks.length, 0);
}

/**
 * Search hooks by keyword across all categories
 */
export function searchHooks(keyword: string): { category: string; hook: string }[] {
  const results: { category: string; hook: string }[] = [];
  for (const template of HOOK_TEMPLATES) {
    for (const hook of template.hooks) {
      if (hook.includes(keyword)) {
        results.push({ category: template.category, hook });
      }
    }
  }
  return results;
}
